        //processRequest(request, response);
           int codigo = Integer.parseInt(request.getParameter("txtcodigo"));
        String nombreAsignatura = request.getParameter("txtnombre");
        int cantidadHoras = Integer.parseInt(request.getParameter("txtcantidadhoras"));
        String carrera = request.getParameter("txtcarrera");
       
        AsignaturaDTO nuevaAsignatura = new AsignaturaDTO(codigo, nombreAsignatura , cantidadHoras, carrera);
        AsignaturaDAO nuevoRegistro2 = new AsignaturaDAO();
        if(nuevoRegistro2.create(nuevaAsignatura))
        {
            request.getRequestDispatcher("insertado.jsp").forward(request, response);
        }
        else
        {
            request.getRequestDispatcher("NOinsertado.jsp").forward(request, response);
        }
